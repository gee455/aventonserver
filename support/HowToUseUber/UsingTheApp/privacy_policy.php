        certain instances the browser may prompt you for your geo-location to allow us to provide you with an enhanced experience. With this technical data our
        website administrators can manage the Website, for instance by resolving technical difficulties or improving the accessibility of certain parts of the
        Website. This way, we ensure that you can (continue to) find the information on the Website in a quick and simple manner.
    </p>
    <p>
        For this purpose we also use cookies. See paragraph 8 for our cookie policy.
    </p>
    <p>
        When you use our App, we also process technical data such as your IP-address, Device ID or MAC- address, and information about the manufacturer, model,
        and operating system of your mobile device. We use this data to enable us to deliver the functionalities of the App, resolve technical difficulties,
        provide you with the correct and most up to date version of the App and to improve the function of the App.
    </p>
    <p>
        3.2 CUSTOMER SERVICE
    </p>
    <p>
        When you register as a User, we will collect your name, email address, country, language, password, mobile phone number, IP-address, MAC-address, and
        your credit card number, expiry date and security code.
