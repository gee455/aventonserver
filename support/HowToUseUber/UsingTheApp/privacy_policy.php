<div id="page_3">
    <p>
        3.3 ENHANCING YOUR POUND CABS EXPERIENCE
    </p>
    <p>
        GEO-LOCATION INFORMATION
    </p>
    <p>
        When you use the App through your mobile device, and only if you have consented thereto, we will track your geo-location information, on a real time
        basis only. We use this information to allow you to view the Drivers in your area that are close to your location, set your pick up location and send
        it to the requested Driver, and see the requested Driver approaching on a map in real time. We may also use this real-time geo-location information to
        address user or driver support, technical, or business issues that may arise in the course of your use of the App. If you contact Pound Cabs customer
        service while on a ride, and it is necessary to use your real-time geo-location information to address your question, our support personnel will seek
        your consent before consulting your real-timegeo-location. In addition, your choice to use some of the features of the App during the ride, for
        example, 'Share my Ride' will allow you to share your geo-location information with those persons with whom you choose to share such information.
    </p>
    <p>
        You can also decide to share your estimated time of arrival (ETA) with one of your contacts. In that case the App will automatically send your contact
        a one-time text message from your contact list informing him or her of your ETA.
    </p>
    <p>
        If you do not consent to the tracking of your geo-location, you will still be able to use the basic features of the App. You may at any time withdraw
        your consent in your Pound Cabs account. Further, you can turn the geo-location tracking off at the device level, also temporarily. Your mobile device
        informs you when geo-location is tracked by displaying the GPS arrow icon.
    </p>
    <p>
        If you are traveling in a Vehicle, we will collect the Driver's geo-location information from the Driver's mobile device. Because you are travelling in
        the Vehicle, this means we will also indirectly collect your location information. This information is necessary to calculate your fare, send you a
        receipt showing the route of the ride, allow you to view your trip history in your Pound Cabs account, and handle complaints from you or Drivers. We will
        also use the Driver's geo-location information in an anonymised and aggregated form in order to generate statistical information and management
        information and to provide you with increased App functionality.
    </p>
